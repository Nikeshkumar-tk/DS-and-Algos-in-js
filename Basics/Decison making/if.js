/*                                                   if statements in javascript
                                                     ----------------------------
  Syntax
          if(condition){
          
              block of code;
          
            }
            
If the condition given in the parentheses followed by if keyword evaluates to true, then the block of code is executed else, it is skipped and control is transffered to the next line.

 */

//example : 

 const age = 18
 if(age >= 18){
   console.log("Eligible for votting")
 }

//Running the above example will print "Eligible for votting" since tthe value of variable "age" is 18

