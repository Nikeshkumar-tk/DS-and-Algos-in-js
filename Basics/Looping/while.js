/*                                                while loop in javascript
                                                  -------------------------
 Syntax : 

               while(condition){
               
                  block of statement;
                  
               }

 - In javascript, while loop is similar to while loop in any other programming languages like c, c++, python etc.
 - It executes a given block of statement continuosly as long as the given condition evaluates to true
 - When the condition becomes fasle, control is transfered to the next line.



*/


